#include <string.h>
#include "libft.h"

size_t			ft_strlen(const char *s)
{
	size_t		cpt;

	cpt = 0;
	if (s)
		while (*s != '\0')
		{
			cpt++;
			s++;
		}
	return (cpt);
}
