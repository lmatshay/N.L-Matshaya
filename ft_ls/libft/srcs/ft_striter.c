#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int		cpt;

	cpt = 0;
	if (s != NULL && f != NULL)
	{
		while (s[cpt] != '\0')
		{
			f(&s[cpt]);
			cpt++;
		}
	}
}
