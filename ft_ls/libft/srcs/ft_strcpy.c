#include "libft.h"

char		*ft_strcpy(char *s1, const char *s2)
{
	char	*tmp;

	if (!s1 || !s2)
		return (NULL);
	tmp = s1;
	while (*s2 != '\0')
	{
		*s1 = *s2;
		s2++;
		s1++;
	}
	*s1 = '\0';
	s1 = tmp;
	return (s1);
}
