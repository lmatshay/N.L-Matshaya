#include "../minishell.h"

void	del_strs(char ***strs)
{
	int	loop;

	loop = 0;
	while (strs[0][loop] != '\0')
	{
		str_del(&(strs[0][loop]));
		loop++;
	}
	free(strs[0]);
	strs[0] = NULL;
}
