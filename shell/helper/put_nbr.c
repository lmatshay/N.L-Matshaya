#include "../minishell.h"

void	ft_putnbr(int nb)
{
	if (nb < 0)
	{
		put_chr('-');
		nb = -nb;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
	{
		put_chr(nb + '0');
	}
}
