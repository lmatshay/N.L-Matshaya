#include "../minishell.h"

void	str_cpy(char *dst, char *src)
{
	int	loop;

	loop = 0;
	while (src[loop] != '\0')
	{
		dst[loop] = src[loop];
		loop++;
	}
	dst[loop] = '\0';
}
