#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list		*tmp;

	tmp = (*alst);
	while (tmp)
	{
		del((tmp->content), (tmp->content_size));
		free(tmp);
		tmp = tmp->next;
	}
	free(tmp);
	tmp = NULL;
	(*alst) = NULL;
	alst = NULL;
}
