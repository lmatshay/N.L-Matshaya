#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*cpy;
	int		i;

	i = 0;
	cpy = NULL;
	while (s1[i] != '\0')
		i++;
	cpy = (char *)malloc(sizeof(char) * (1 + i));
	if (cpy == 0)
		return (0);
	i = 0;
	while (s1[i] != '\0')
	{
		cpy[i] = s1[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
