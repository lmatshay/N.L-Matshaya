char	*ft_strrchr(const char *s, int c)
{
	char	a;
	int		i;
	char	*p;

	i = 0;
	a = (char)c;
	p = (char *)s;
	while (p[i] != '\0')
		i++;
	if (a == '\0')
		return (&p[i]);
	while (i >= 0)
	{
		if (p[i] == a)
			return (&p[i]);
		i--;
	}
	return (0);
}
